#[test]
fn it_fails_to_parse_board() {
  let data = "[[";

  let board: Result<BoardData<FLAGSHIP_ROWS, FLAGSHIP_COLS>, BoardError> = data.parse();
  assert!(board.is_err());
}

#[test]
fn it_fails_too_many_rows() {
  let data = "[[],[],[],[],[],[],[]]";

  let board: Result<BoardData<FLAGSHIP_ROWS, FLAGSHIP_COLS>, BoardError> = data.parse();
  assert!(board.is_err());
}

